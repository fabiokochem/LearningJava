package VariaveisFluxos.src;

public class BrincandoComConversao {
    public static void main(String[] args) {
        double valor = 0.2 + 0.1;

        float valor1 = (float) valor;
        double valor2 = valor;

        System.out.println("O valor de pi é: " + valor1);
        System.out.println("O valor de pi é: " + valor2);
    }
}
